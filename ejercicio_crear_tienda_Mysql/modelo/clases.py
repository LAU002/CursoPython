class Prenda():
    def __init__(self, talla = 0, color = "", marca = "", tipo = "", precio = 0.0, \
                    online = False, temporada = "invierno", destino = "peninsula", id = 0 ):
        self.talla = talla
        self.color = color
        self.marca = marca
        self.tipo = tipo
        self.precio = precio
        self.online = online
        self.temporada = temporada
        self.destino = destino
        self.id = id